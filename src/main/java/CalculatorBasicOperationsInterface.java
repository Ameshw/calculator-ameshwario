
public interface CalculatorBasicOperationsInterface {

	/**
	 * function to compute addition of x and y
	 * @param x input x
	 * @param y input y
	 * @return output of addition
	 */
	public double add(double x, double y);
	
	/**
	 * function to copmute subtraction of x and y
	 * @param x input x
	 * @param y input y
	 * @return output of subtraction
	 */
	public double substract(double x, double y);
	
	/**
	 * function to compute multiplication of x and y
	 * @param x input x
	 * @param y input y
	 * @return output of multiplication
	 */
	public double multiply(double x, double y);
	
	/**
	 * function to compute division of x by y
	 * @param x input x
	 * @param y input y
	 * @return output of division
	 */
	public double divide(double x, double y);

}
